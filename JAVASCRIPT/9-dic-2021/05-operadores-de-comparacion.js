/*
OPERADPRES RACIONALES O DE COMPARACION
Mayor que: a>b                                a es mayor que b
Menor que: b<b                                a es menor que b
Mayor o igual que : a>=b                      a es mayor o igual que b
Menor o igual que : a<=b                      a es menor o igual a b
Igual: ==                                     a es igual que b  
Distinto !=                                   a es diferente de b
Operador estricto  ===
Operador diferente estricto  !==

*/

let numero1 = 22;
let numero2 = 10;

console.log('Mayor que: ' + (numero1 > numero2)); /// poner entre parentesis las operaciones                 /// igual se puede poner una coma ","
console.log('Menor que: b<b : ' + (numero1 < numero2)); /// poner entre parentesis las operaciones
console.log('Mayor o igual que : ' + (numero1 >= numero2));
console.log('Menor o igual que : ' + (numero1 <= numero2));
console.log('Igual: ' + (numero1 == numero2));
console.log('Distinto !=   ' + (numero1 != numero2));